import { execFile as execFileCallback } from "node:child_process";
import { mkdtemp, readFile, rm, writeFile } from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { promisify } from "node:util";

const execFile = promisify(execFileCallback);
const repositoryRoot = path.resolve(import.meta.dirname, "..");
const manifestPath = path.join(repositoryRoot, "scripts", "phosphor-icons.txt");
const outputPath = path.join(
    repositoryRoot,
    "Watermark.Razor",
    "Components",
    "Compatibility",
    "WmPhosphorIconPaths.g.cs");
const packageVersion = "2.1.1";
const temporaryDirectory = await mkdtemp(path.join(os.tmpdir(), "wm-phosphor-"));

try {
    const names = (await readFile(manifestPath, "utf8"))
        .split(/\r?\n/)
        .map((name) => name.trim())
        .filter(Boolean);

    if (new Set(names).size !== names.length) {
        throw new Error("Phosphor icon manifest contains duplicate names.");
    }

    const { stdout } = await execFile(
        "npm",
        ["pack", `@phosphor-icons/core@${packageVersion}`, "--pack-destination", temporaryDirectory],
        { cwd: repositoryRoot });
    const archiveName = stdout.trim().split(/\r?\n/).at(-1);
    const archivePath = path.join(temporaryDirectory, archiveName);
    await execFile("tar", ["-xzf", archivePath, "-C", temporaryDirectory]);

    const entries = [];
    for (const name of names) {
        const regular = await readPhosphorPath(
            path.join(temporaryDirectory, "package", "assets", "regular", `${name}.svg`));
        const light = await readPhosphorPath(
            path.join(temporaryDirectory, "package", "assets", "light", `${name}-light.svg`));
        entries.push({ name, regular, light });
    }

    const generated = `// <auto-generated />
// Generated by scripts/generate-phosphor-icons.mjs from @phosphor-icons/core ${packageVersion}.

namespace Watermark.Razor.Components.Compatibility;

public static class WmPhosphorIconPaths
{
    public const string SourceVersion = "${packageVersion}";

    public static string Get(string name, string weight = "regular")
    {
        var useLightWeight = string.Equals(weight, "light", StringComparison.OrdinalIgnoreCase);
        return name switch
        {
${entries.map(({ name, regular, light }) =>
    `            "${name}" => useLightWeight ? "${escapeCSharp(light)}" : "${escapeCSharp(regular)}",`).join("\n")}
            _ => throw new ArgumentOutOfRangeException(nameof(name), name, "Unsupported Phosphor icon.")
        };
    }
}
`;

    await writeFile(outputPath, generated);
    console.log(`Generated ${entries.length} Phosphor icons at ${outputPath}`);
} finally {
    await rm(temporaryDirectory, { recursive: true, force: true });
}

async function readPhosphorPath(filePath) {
    const svg = await readFile(filePath, "utf8");
    const pathData = [...svg.matchAll(/<path\b[^>]*\bd="([^"]+)"[^>]*\/?>/g)].map((match) => match[1]);
    if (pathData.length === 0) {
        throw new Error(`No flattened SVG path found in ${filePath}`);
    }

    const unsupportedMarkup = svg
        .replace(/<svg\b[^>]*>/, "")
        .replace(/<\/svg>/, "")
        .replace(/<path\b[^>]*\/?>/g, "")
        .trim();
    if (unsupportedMarkup.length > 0) {
        throw new Error(`Unsupported non-path SVG markup found in ${filePath}`);
    }

    return pathData.join(" ");
}

function escapeCSharp(value) {
    return value.replaceAll("\\", "\\\\").replaceAll("\"", "\\\"");
}
